
// below is an example of a for loop that counts from 1 to 5
for (var i=1; i<6; i++) {
    console.log(i);
};
console.log("Counting completed");

//for loop that counts from 5 to 1
for (var i=5; i>0; i--){
    console.log(i);
};
console.log("Counting completed");


//while loop that counts from 1 to 5
var counter = 1;
while (counter < 6){
    console.log(counter);
    counter += 1;
};
console.log("Counting completed");


//while loop that counts down from 5 to 1
var counter = 5;
while (counter > 0) {
    console.log(counter);
    counter -= 1
};
console.log("Countdown Completed");


